SUBROUTINE TERMIN1

USE IOUNITS
USE DEBUGTMP
USE MPIREL, ONLY : LLUSEMPI

IMPLICIT NONE

INTEGER(I4) :: IERR

CALL FLUSH(IOUNLOG)
CLOSE(IOUNLOG)

CALL FLUSH(IOUNCOST)
CLOSE(IOUNCOST)

CALL FLUSH(IOUNOUT)

IF( LL_DEBUGTMP ) CALL CALLDT(1)

IF(LLUSEMPI) CALL MPI_FINALIZE(IERR)

END SUBROUTINE TERMIN1

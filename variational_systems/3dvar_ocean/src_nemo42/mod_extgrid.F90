CALL DEWRAP(ZTEMP4C,ROS%EVC,GRD%JM,ROS%KMT,ROS%NEOF)

#endif
CALL FLUSH(IOUNLOG)

! PSV
ZTEMP3F=PSV
DEALLOCATE(PSV)
ALLOCATE(PSV(OLD_IM,GRD%JM,PSV3D*GRD%KM+PSV2D))
CALL DEWRAP(ZTEMP3F,PSV,GRD%JM,PSV3D*GRD%KM+PSV2D)
CALL FLUSH(IOUNLOG)

NULLIFY(GRD%TEM)
NULLIFY(GRD%SAL)
NULLIFY(GRD%UVL)
NULLIFY(GRD%VVL)
NULLIFY(GRD%ETA)
NULLIFY(GRD%SST)
NULLIFY(GRD%SSS)
NULLIFY(GRD%T2M)
NULLIFY(GRD%Q2M)
NULLIFY(GRD%SIC)
NULLIFY(GRD%SIT)
NULLIFY(GRD%ICU)
NULLIFY(GRD%ICV)
NULLIFY(GRD%TEM_AD)
NULLIFY(GRD%SAL_AD)
NULLIFY(GRD%UVL_AD)
NULLIFY(GRD%VVL_AD)
NULLIFY(GRD%ETA_AD)
NULLIFY(GRD%SST_AD)
NULLIFY(GRD%SSS_AD)
NULLIFY(GRD%T2M_AD)
NULLIFY(GRD%Q2M_AD)
NULLIFY(GRD%SIC_AD)
NULLIFY(GRD%SIT_AD)
NULLIFY(GRD%ICU_AD)
NULLIFY(GRD%ICV_AD)
CALL FLUSH(IOUNLOG)

     KSV=0

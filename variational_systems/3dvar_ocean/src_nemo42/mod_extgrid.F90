SUBROUTINE EXTGRID_INIT

OLD_IM = GRD%IM
NEW_IM = OLD_IM + NWRAPS*2 - 2
FRST = NWRAPS + 1
LST = NEW_IM - NWRAPS
FRST2 = NWRAPS
LST2 = LST + 1

LLINCALLED=.TRUE.

END SUBROUTINE EXTGRID_INIT


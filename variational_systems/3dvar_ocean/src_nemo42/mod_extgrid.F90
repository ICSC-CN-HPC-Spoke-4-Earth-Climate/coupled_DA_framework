CALL DEWRAP(ZTEMP3,GRD%MSR,GRD%JM,GRD%KM)

IF( DRV%BAL(DRV%KTR) .EQ. 1 ) THEN
    ZTEMP3=BAL_TEMB
    DEALLOCATE(BAL_TEMB)
    ALLOCATE(BAL_TEMB(OLD_IM,GRD%JM,GRD%KM))
    CALL DEWRAP(ZTEMP3,BAL_TEMB,GRD%JM,GRD%KM)

    ZTEMP3=BAL_SALB
    DEALLOCATE(BAL_SALB)
    ALLOCATE(BAL_SALB(OLD_IM,GRD%JM,GRD%KM))
    CALL DEWRAP(ZTEMP3,BAL_SALB,GRD%JM,GRD%KM)
ENDIF

IF(NCONF .NE. 202) THEN
! RO
ZTEMP3B=GRD%RO
DEALLOCATE(GRD%RO)
ALLOCATE(GRD%RO(OLD_IM,GRD%JM,ROS%NEOF))
CALL DEWRAP(ZTEMP3B,GRD%RO,GRD%JM,ROS%NEOF)

! RO_AD
ZTEMP3B=GRD%RO_AD
DEALLOCATE(GRD%RO_AD)
ALLOCATE(GRD%RO_AD(OLD_IM,GRD%JM,ROS%NEOF))
CALL DEWRAP(ZTEMP3B,GRD%RO_AD,GRD%JM,ROS%NEOF)
ENDIF

CALL FLUSH(IOUNLOG)

IF(NCONF .NE. 202) THEN
#ifdef opt_huge_memory

! EVA
ZTEMP3B=ROS%EVA
DEALLOCATE(ROS%EVA)
ALLOCATE(ROS%EVA(OLD_IM,GRD%JM,ROS%NEOF))
CALL DEWRAP(ZTEMP3B,ROS%EVA,GRD%JM,ROS%NEOF )

! EVC

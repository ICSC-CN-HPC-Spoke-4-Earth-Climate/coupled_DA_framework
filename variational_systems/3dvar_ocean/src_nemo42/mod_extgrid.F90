         INS%OTYPE(K) = INS%OTYPE(I)
         INS%PAR  (K) = INS%PAR  (I)
         INS%PLNO (K) = INS%PLNO (I)
         INS%KTY  (K) = INS%KTY  (I)
         INS%LON  (K) = INS%LON  (I)
         INS%LAT  (K) = INS%LAT  (I)
         INS%DPT  (K) = INS%DPT  (I)
         INS%TIM  (K) = INS%TIM  (I)
         INS%VAL  (K) = INS%VAL  (I)
         INS%BAC  (K) = INS%BAC  (I)
         INS%INC  (K) = INS%INC  (I)
         INS%BIA  (K) = INS%BIA  (I)
         INS%ERR  (K) = INS%ERR  (I)
         INS%RES  (K) = INS%RES  (I)
         INS%IB (K,:) = INS%IB   (I,:)-(OLD_IM-NWRAPS)+1
         INS%JB (K,:) = INS%JB   (I,:)
         INS%PQ (K,:) = INS%PQ   (I,:)
         INS%KB   (K) = INS%KB   (I)
         INS%BGERR(K) = INS%BGERR(I)
         INS%RB   (K) = INS%RB   (I)
         INS%PROF (K) = INS%PROF (I)
         INS%B_A  (K) = INS%B_A  (I)
         INS%EVE  (K) = INS%EVE  (I)
         INS%INST (K) = INS%INST (I)
         INS%INO  (K) = INS%INO  (I)
         INS%FLG  (K) = INS%FLG  (I)
         INS%NIND (K) = INS%NIND (I)
         INS%DSRC (K) = INS%DSRC (I)
      ENDIF

      IF(INS%IB(I,1).LE.NWRAPS+1 .AND. LLDUPLOBS .AND. &
       & INS%IB(I,1)+LST2-2 .LT. NEW_IM ) THEN ! NEW OBS

         K=K+1
         IF(K.GT.NMAX) CALL ABOR1('DOOBSREARR: NMAX SHOULD BE INCREASED')
         INS%FLC(K) = 1
         INS%TDIST(K) = INS%TDIST(I)
         INS%OTYPE(K) = INS%OTYPE(I)
         INS%PAR  (K) = INS%PAR  (I)
         INS%PLNO (K) = INS%PLNO (I)
         INS%KTY  (K) = INS%KTY  (I)
         INS%LON  (K) = INS%LON  (I)
         INS%LAT  (K) = INS%LAT  (I)
         INS%DPT  (K) = INS%DPT  (I)
         INS%TIM  (K) = INS%TIM  (I)
         INS%VAL  (K) = INS%VAL  (I)
         INS%BAC  (K) = INS%BAC  (I)
         INS%INC  (K) = INS%INC  (I)
         INS%BIA  (K) = INS%BIA  (I)
         INS%ERR  (K) = INS%ERR  (I)
         INS%RES  (K) = INS%RES  (I)
         INS%IB (K,:) = INS%IB   (I,:)+LST2-2
         INS%JB (K,:) = INS%JB   (I,:)
         INS%PQ (K,:) = INS%PQ   (I,:)
         INS%KB   (K) = INS%KB   (I)
         INS%BGERR(K) = INS%BGERR(I)
         INS%RB   (K) = INS%RB   (I)
         INS%PROF (K) = INS%PROF (I)
         INS%B_A  (K) = INS%B_A  (I)
         INS%EVE  (K) = INS%EVE  (I)
         INS%INST (K) = INS%INST (I)
         INS%INO  (K) = INS%INO  (I)
         INS%FLG  (K) = INS%FLG  (I)
         INS%NIND (K) = INS%NIND (I)
         INS%DSRC (K) = INS%DSRC (I)
      ENDIF

      INS%IB(I,:)=INS%IB(I,:)+OBS_DIFF
      IF(ANY(INS%IB(I,:).EQ.NEW_IM) ) INS%FLC(I)=0

    ENDIF
   ENDDO
   WRITE(IOUNOUT,*) ' ORIGINAL      INS : ',INS%NO
   WRITE(IOUNOUT,*) ' DUPLICATED    INS : ',K-INS%NO
   OLD_INS_NO=COUNT(INS%FLC(1:INS%NO)==1)
   !! OLD_INS_NO=INS%NO
   WRITE(IOUNOUT,*) ' ORIGINAL GOOD INS : ',OLD_INS_NO
   INS%NO=K
   ENDIF

WRITE(IOUNOUT,*) '                 DONE!'

CALL FLUSH(IOUNLOG)
CALL FLUSH(IOUNOUT)

CALL MYFRTPROF_WALL('DOOBSREARR: REARRANGE OBS FOR EXTENDED GRID',1)

END SUBROUTINE DOOBSREARR


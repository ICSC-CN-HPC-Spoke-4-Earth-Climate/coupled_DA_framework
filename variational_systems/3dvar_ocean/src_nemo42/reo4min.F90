   NEWINS%PQ(K,:) =  INS%PQ(JOBS,:)
   NEWINS%NIND(K) = INS%NIND(JOBS)
   NEWINS%BGERR(K) = INS%BGERR(JOBS)
   NEWINS%DSRC(K) = INS%DSRC(JOBS)
   NEWINS%IBH(K,:) = INS%IBH(JOBS,:)

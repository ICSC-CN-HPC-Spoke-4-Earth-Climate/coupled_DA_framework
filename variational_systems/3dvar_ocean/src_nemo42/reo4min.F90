        &NEWSST%JB (NEWSST%NO,NPQ),&
        &NEWSST%PQ(NEWSST%NO,NPQ),&
        &NEWSST%IBH(NEWSST%NO,NPQ),&
        &NEWSST%JBH(NEWSST%NO,NPQ),&
        &NEWSST%PQH(NEWSST%NO,NPQ),&
        &NEWSST%TRACK(NEWSST%NO),&
        &NEWSST%BCP(NEWSST%NO,NPRD_SST),&
        &NEWSST%TIM(NEWSST%NO))


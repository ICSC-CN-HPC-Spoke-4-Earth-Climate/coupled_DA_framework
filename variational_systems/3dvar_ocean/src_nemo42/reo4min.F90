SUBROUTINE REO4MIN

!*** REO4MIN
!
! PURPOSE: SAFELY REORDER OBS VECTORS IN ORDER
!  TO HAVE ONLY GOOD OBSERVATIONS IN THE NEW
!  OBS ARRAYS. THIS SPEEDS UP MINIMIZATION,
!  ESPECIALLY WITHIN OPENMP PARALLELIZATION.
!
! A.STORTO, 2009


TYPE(SLA_T) :: NEWSLA
TYPE(INS_T) :: NEWINS
TYPE(SST_T) :: NEWSST
TYPE(SSS_T) :: NEWSSS


CALL MYFRTPROF_WALL('REO4MIN: REORDER OBS VECTOR',0)

!... SLA CASE
